def cep():
    import requests

    print('\033[1;34m####################')
    print('\033[1;34m### consulta CEP ###')
    print('\033[1;34m####################')
    print()
    cep_input = input('Digite o CEP : ')
    
    if len(cep_input) != 8:
        print('quantidade de digito errada')
        exit()

    request = requests.get('https://viacep.com.br/ws/{}/json/'.format(cep_input))

    address_data = request.json()

    if 'erro' not in address_data:
        print('\033[1;34m--> cep encontrado <--')

        print('\033[1;31m[> CEP <] : {}'.format(address_data['cep']))
        print('\033[1;31m[> Logradouro <] : {}'.format(address_data['logradouro']))
        print('\033[1;31m[> Complemento <] : {}'.format(address_data['complemento']))
        print('\033[1;31m[> Bairro <] : {}'.format(address_data['bairro']))
        print('\033[1;31m[> Localidade <] : {}'.format(address_data['localidade']))
        print('\033[1;31m[> U.F <] : {}'.format(address_data['uf']))
        print('\033[1;31m[> DDD <] : {}'.format(address_data['ddd']))

    else:
        print('{} : CEP invalido'.format(cep_input))
